package Basicspgms;

public class Looping {
		
		//Looping ---> forloop, while loop, do while loop, for each

		public static void main(String[] args) {
			
			
			/*for(initialization; condition; increment or decrement)
			{
				//logic
			}*/
			
			for(int i=1; i<=10; i++)  
			{
				System.out.println(i);
			}
			
			System.out.println("-------------");
			
			for(int i=10; i>=1; i--)
			{
				System.out.println(i);
			}
		}

	}



